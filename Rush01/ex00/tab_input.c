void	ft_tab_input(char tab[4][4], char *input)
{
	int	x;
	int	y;
	int	i;

	i = 0;
	y = 0;
	while (y < 4)
	{
		x = 0;
		while (x < 4)
		{
			tab[y][x] = input[i];
			i = i + 2;
			x++;
		}
		y++;
	}
}
