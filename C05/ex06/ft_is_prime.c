int	ft_is_prime(int nb)
{
	int	i;

	if (nb < 2)
		return (0);
	if (nb == 2 || nb == 3)
		return (1);
	if (nb > 2)
	{
		i = 2;
		while ((nb / i) >= i)
		{
			if ((nb % i) == 0)
				return (0);
			i++;
		}
		return (1);
	}
	return (42);
}
